package com.example;

public class Pancard2 {
	private int panno;
	private String panname;

	public Pancard2(int panno, String panname) {
		super();
		this.panno = panno;
		this.panname = panname;
	}
	@Override
	public String toString() {
		return "Pancard2 [panno=" + panno + ", panname=" + panname + "]";
	}
}
